package Exemplos.POO;

public class POO_01_Conta {
    public static int soma(int a, int b) {
    int soma = a + b;
    return soma;
    }

    public static int subt (int a, int b){
        int subt= a-b;
        return subt;
    }

    public static int mult (int a, int b){
        int mult= a*b;
        return mult;
    }

    public static int div (int a, int b){
        int div= a/b;
        return div;
    }
}
